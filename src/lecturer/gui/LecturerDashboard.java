    /**
     * Retrieves and displays the count of students and courses from the database.
     */
    public void userCounts() {
        PreparedStatement pst1 = null;
        PreparedStatement pst2 = null;
        ResultSet rs1, rs2;
        int stu_count = 0, course_count = 0;

        try {
            dbConnector = new MyDbConnector();
            System.out.println("Succeed...");
            try {
                connection = dbConnector.getMyConnection();

                String sql1 = "SELECT COUNT(id) FROM `userprofiles` WHERE userType = 'student'";
                pst1 = connection.prepareStatement(sql1);
                rs1 = pst1.executeQuery();

                String sql2 = "SELECT COUNT(CourseID) FROM `coursedetails`";
                pst2 = connection.prepareStatement(sql2);
                rs2 = pst2.executeQuery();

                if (rs1.next()) {
                    stu_count = rs1.getInt(1);
                }
                if (rs2.next()) {
                    course_count = rs2.getInt(1);
                }

                student_count = Integer.toString(stu_count);
                courses_count = Integer.toString(course_count);
                student_count = Integer.toString(stu_count);

                sttuu_count.setText(student_count);  
                coursse_count.setText(courses_count);

                System.out.println("Student count: " + student_count);
                System.out.println("Courses count: " + courses_count);
            } catch (Exception e) {
                System.out.println("Getting error connect...");
            } finally {
                if (connection != null) {
                    try {
                        connection.close();
                        System.out.println("Close....");
                    } catch (SQLException e) {
                        System.out.println("Error in closing the connection: " + e.getMessage());
                    }
                }
            } 
        } catch (Exception e) {
            System.out.println("dbConnector not assigned.." + e.getMessage());
        }
    }

